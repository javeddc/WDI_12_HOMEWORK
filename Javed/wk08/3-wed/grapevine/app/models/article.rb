  def process_article
    # binding.pry
    words = self.content.split(' ')
    new_words = []
    words.each do |word|
      word.gsub!(/ \,\.-'"/, '')
      word.downcase!
    end
    words.each do |word|
      if !@@stop_words.include? word
        # binding.pry
        new_words.push word
      end
    end
    # binding.pry
    histogram = {}
    new_words.each do |word|
      if histogram[word] == nil
        histogram[word] = 1
      else
        histogram[word] += 1
      end
    end
    output = {labels: [], datasets: [{data: []}]}
    histogram.each do |key, value|
      # binding.pry
      output[:labels].push(key)
      output[:datasets][0][:data].push(value)
    end
